import pandas as pd
import numpy as np
from sklearn.linear_model import LogisticRegression
from collections import defaultdict

# ----------------------------
# LOAD DATA
# ----------------------------
lap_data = pd.read_csv("LapTimes.csv")
results_data = pd.read_csv("RaceResults.csv")

# ----------------------------
# FEATURE ENGINEERING
# ----------------------------

# Aggregate lap stats per driver per race
lap_features = lap_data.groupby(['raceId', 'driverId']).agg(
    mean_lap=('lapTime', 'mean'),
    std_lap=('lapTime', 'std'),
    best_lap=('lapTime', 'min')
).reset_index()

# Merge with race results
data = results_data.merge(lap_features, on=['raceId', 'driverId'])

# Create target variable
data['win'] = (data['position'] == 1).astype(int)

# Drop missing values
data = data.dropna()

# ----------------------------
# BOOTSTRAPPED LOGISTIC MODEL
# ----------------------------

def bootstrap_logistic(track_data, n_bootstrap=500):
    drivers = track_data['driverId'].unique()
    prob_distributions = {d: [] for d in drivers}

    features = ['mean_lap', 'std_lap', 'best_lap', 'grid']

    for _ in range(n_bootstrap):
        # Resample with replacement
        sample = track_data.sample(frac=1, replace=True)

        X = sample[features]
        y = sample['win']

        # Train model
        model = LogisticRegression(max_iter=1000)
        model.fit(X, y)

        # Predict probabilities for all drivers in this track
        X_all = track_data[features]
        probs = model.predict_proba(X_all)[:, 1]

        for driver, prob in zip(track_data['driverId'], probs):
            prob_distributions[driver].append(prob)

    return prob_distributions

# ----------------------------
# RUN PER TRACK
# ----------------------------

track_probabilities = {}

for track in data['circuitId'].unique():
    track_data = data[data['circuitId'] == track]

    # Require enough samples
    if len(track_data) < 50:
        continue

    prob_dist = bootstrap_logistic(track_data)

    # Aggregate results
    summary = {}
    for driver, probs in prob_dist.items():
        summary[driver] = {
            'mean_prob': np.mean(probs),
            'std_prob': np.std(probs)
        }

    track_probabilities[track] = summary

# ----------------------------
# DISPLAY RESULTS
# ----------------------------

for track, drivers in track_probabilities.items():
    print(f"\nTrack {track}")
    sorted_drivers = sorted(drivers.items(), key=lambda x: x[1]['mean_prob'], reverse=True)

    for driver, stats in sorted_drivers:
        print(f"Driver {driver}: {stats['mean_prob']:.3f} ± {stats['std_prob']:.3f}")

