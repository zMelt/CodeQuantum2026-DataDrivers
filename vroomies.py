def simulate_race(driver_stats, num_laps=60):
    race_times = {}

    for driver, (mean, std) in driver_stats.items():
      # simulate laps
      laps = np.random.normal(mean, std, num_laps)

      # Fix: Use explicit multiplication operator *
      laps = np.clip(laps, mean - 3 * std, mean + 3 * std)

      total_time = laps.sum()
      race_times[driver] = total_time

    # winner = lowest total time
    winner = min(race_times, key=race_times.get)
    return winner

