race_results['Points'] = race_results['Points'].fillna(0)

# Create historical features for all races
def prepare_features_all(df):
    """Prepare features for all historical data"""
    features = pd.DataFrame()

    features['qualifying_time'] = df['Qual']
    features['grid_position'] = df['GridPosition']
    features['race_round'] = df['Round']

    # Fill missing values
    features = features.fillna(features.mean())

    return features

# Train models on all historical data
def train_historical_models(df_historical, n_bootstrap=500):
    """
    Train models on all historical race data
    """
    X_historical = prepare_features_all(df_historical)

    models = {}

    targets = {
        'Win (1st)': 'Won',
        'Podium (1st-3rd)': 'Podium',
        'Top 5': 'Top5'
    }

    for target_name, target_col in targets.items():
        print(f"\nTraining model for {target_name}...")
        y_historical = df_historical[target_col]

        # Check if we have both classes
        if y_historical.sum() < 3 or (len(y_historical) - y_historical.sum()) < 3:
            print(f"  Not enough data for {target_name}, using simple heuristic")
            models[target_name] = None
            continue

        # Bootstrap to get multiple models
        model_list = []
        scaler_list = []

        for i in range(n_bootstrap):
            # Resample with replacement
            indices = resample(range(len(X_historical)),
                              n_samples=len(X_historical),
                              replace=True,
                              random_state=i)
            X_boot = X_historical.iloc[indices]
            y_boot = y_historical.iloc[indices]

            # Check if we have both classes in bootstrap sample
            if len(np.unique(y_boot)) < 2:
                continue

            # Scale features
            scaler = StandardScaler()
            X_scaled = scaler.fit_transform(X_boot)

            # Train model
            model = LogisticRegression(max_iter=1000, random_state=i)
            model.fit(X_scaled, y_boot)

            model_list.append(model)
            scaler_list.append(scaler)

            if (i + 1) % 100 == 0:
                print(f"  Completed {i + 1}/{n_bootstrap} bootstraps")

        models[target_name] = {
            'models': model_list,
            'scalers': scaler_list,
            'features': X_historical.columns.tolist()
        }

        print(f"  Trained {len(model_list)} models")

    return models

# Predict for a specific race
def predict_race_outcomes(race_data, historical_models, n_bootstrap=500):
    """
    Predict outcomes for a specific Grand Prix using historical models
    """
    print(f"\n{'='*60}")
    print(f"PREDICTIONS FOR: {race_data['Event Name'].iloc[0]} (Round {int(race_data['Round'].iloc[0])})")
    print(f"{'='*60}\n")

    # Prepare features for this race
    X_race = prepare_features_all(race_data)

    all_predictions = {}

    for target_name, model_dict in historical_models.items():
        print(f"\n--- Predicting {target_name} ---")

        if model_dict is None:
            # Use simple heuristic
            print("  Using heuristic-based prediction")
            race_data_copy = race_data.copy()
            race_data_copy['prob'] = 1 / (race_data_copy['GridPosition'] + 3)
            race_data_copy['prob'] = race_data_copy['prob'] / race_data_copy['prob'].max()

            pred_df = race_data_copy[['FullName', 'DriverId', 'TeamName', 'GridPosition', 'prob']].copy()
            pred_df['probability'] = pred_df['prob']
            pred_df['prob_lower'] = pred_df['prob']
            pred_df['prob_upper'] = pred_df['prob']

            all_predictions[target_name] = pred_df.sort_values('probability', ascending=False)
            continue

        # Make predictions with all bootstrapped models
        all_probs = []

        for model, scaler in zip(model_dict['models'], model_dict['scalers']):
            X_scaled = scaler.transform(X_race)
            probas = model.predict_proba(X_scaled)[:, 1]
            all_probs.append(probas)

        if not all_probs:
            print("  No valid predictions, using heuristic")
            continue

        all_probs = np.array(all_probs)

        # Calculate statistics
        mean_probs = np.mean(all_probs, axis=0)
        lower_ci = np.percentile(all_probs, 2.5, axis=0)
        upper_ci = np.percentile(all_probs, 97.5, axis=0)

        # Create prediction dataframe
        pred_df = race_data[['FullName', 'DriverId', 'TeamName', 'GridPosition']].copy()
        pred_df['probability'] = mean_probs
        pred_df['prob_lower'] = lower_ci
        pred_df['prob_upper'] = upper_ci

        # Sort by probability
        pred_df = pred_df.sort_values('probability', ascending=False)

        all_predictions[target_name] = pred_df

        # Display top predictions
        print(f"\nTop 10 probabilities for {target_name}:")
        print(pred_df[['FullName', 'TeamName', 'GridPosition', 'probability']].head(10).to_string(index=False))
        print(f"\n{'-'*60}")

    return all_predictions

